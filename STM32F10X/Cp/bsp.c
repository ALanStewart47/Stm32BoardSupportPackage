void Error_Handler(char *file, uint32_t line);

/*
*********************************************************************************************************
*	函 数 名: bsp_InitIwdg
*	功能说明: 初始化独立看门狗。	去bsp_timer.h注册定时器，和定时时间。
*	形    参：_Reload：重装载值	
*	返 回 值: 无
*********************************************************************************************************
*/
static void bsp_InitIwdg(uint32_t _Reload)
{
  iwdg.Instance = IWDG;
  iwdg.Init.Prescaler = IWDG_PRESCALER_4;
  iwdg.Init.Reload = _Reload;
  if (HAL_IWDG_Init(&iwdg) != HAL_OK)
  {
    Error_Handler(__FILE__, __LINE__);
  }
}
/*
*********************************************************************************************************
*	函 数 名: init_ScheduleTack
*	功能说明: 初始化定时任务，	去bsp_timer.h注册定时器，和定时时间。
*	形    参：无
*	返 回 值: 无
*********************************************************************************************************
*/
void bsp_feedDog(void)
{
	HAL_IWDG_Refresh(&iwdg);
}

/*
*********************************************************************************************************
*	函 数 名: init_ScheduleTack
*	功能说明: 初始化定时任务，	去bsp_timer.h注册定时器，和定时时间。
*	形    参：无
*	返 回 值: 无
*********************************************************************************************************
*/
void init_ScheduleTack(void)
{
	/*	ADC滤波定时器	*/
	bsp_StartAutoTimer(ADC_FILTER_TMR,ADC_FILTER_TIME);			//5ms
	/*	看门口定时器*/
	bsp_StartAutoTimer(IWDG_TMR,IWDG_TIME);						//50ms IWDG_TMR 
	/*	Flash――backup数据备份*/
	bsp_StartTimer(FLASH_BACKUP_TMR,FLASH_BACKUP_TIME);			//一次性定时器，20min	

}

/*
*********************************************************************************************************
*	函 数 名: run_ScheduleTack
*	功能说明: 运行定时任务，需在此函数内检查定时器是否到时间，并添加对应的业务代码
*	形    参：无
*	返 回 值: 无
*********************************************************************************************************
*/
void run_ScheduleTack(void)
{
	/*	保存后备区数据-20min	*/
	if(bsp_CheckTimer(FLASH_BACKUP_TMR))
	{
		bsp_SaveFlashBackupData();	
	}
	/*	ADC滤波处理-5ms,ADC采集所有数据所需的时间大概是2.7ms*/
	if(bsp_CheckTimer(ADC_FILTER_TMR))
	{
		bsp_adc_filter_handler();
	}
	/*	喂狗-50ms	*/
	if(bsp_CheckTimer(IWDG_TMR))
	{
		bsp_feedDog();
	}
}

/*
*********************************************************************************************************
*	函 数 名: bsp_Init
*	功能说明: 初始化所有的硬件设备。该函数配置CPU寄存器和外设的寄存器并初始化一些全局变量。只需要调用一次
*	形    参：无
*	返 回 值: 无
*********************************************************************************************************
*/
void bsp_Init(void)
{
	
	bsp_InitAip650();				/* 初始化数码管显示 */
	bsp_InitLight();				/* 初始化光源通道	*/
	bsp_InitKey();    			/* 按键初始化，要放在滴答定时器之前，因为按钮检测是通过滴答定时器扫描 */
	bsp_InitTimer();  			/* 初始化滴答定时器, 初始化后定时任务会马上执行 */
	bsp_ReadFlashData();		/*	读取FLASH中的参数	*/
	bsp_InitUart();					/* 初始化串口 */
	init_ScheduleTack();		/*	定时任务函数初始化	*/
	MCP4922_Init();					/*	DAC芯片初始化	*/
	//bsp_InitTrigHard();			/*	触发口IO初始化	*/
	bsp_InitIwdg(IWDG_TIME_2S);		/*	初始化独立开门狗： 2s	*/
		
}



/*
*********************************************************************************************************
*	函 数 名: Error_Handler
*	形    参: file : 源代码文件名称。关键字 __FILE__ 表示源代码文件名。
*			  line ：代码行号。关键字 __LINE__ 表示源代码行号
*	返 回 值: 无
*		Error_Handler(__FILE__, __LINE__);
*********************************************************************************************************
*/
void Error_Handler(char *file, uint32_t line)
{
	/* 
		可以添加自己的代码报告源代码文件名和代码行号，比如将错误文件和行号打印到串口
		printf("Wrong parameters value: file %s on line %d\r\n", file, line) 
	*/
	printf("Wrong value: file %s on line %d\r\n", file, line);
	
	/* 这是一个死循环，断言失败时程序会在此处死机，以便于用户查错 */
	if (line == 0)
	{
		return;
	}
	
	while(1)
	{
		bsp_feedDog();
	}
}

